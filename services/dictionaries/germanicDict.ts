export type GermanicEntry = string | readonly [string, 'm' | 'f' | 'n' | 'c']; 

export interface GermanicComponent {
  def: string; 
  type: 'root' | 'suffix' | 'noun_prefix' | 'adjective' | 'country_suffix' | 'connector';
  de?: GermanicEntry; 
  nl?: GermanicEntry; 
  da?: GermanicEntry; 
  sv?: GermanicEntry; 
  // 'weak_en'  -> German: prefers -en in compounds (Altenburg)
  // 'irreg_h'  -> German: Hoch -> Hohen
  // 'no_conn'  -> Don't use connector glue (e.g. for -ing endings)
  tags?: string[];
  
}

export const getGenData = (entry: GermanicEntry | undefined): { val: string, gender?: string } => {
  if (!entry) return { val: '' }; // Handle missing translations gracefully
  
  if (typeof entry === 'string') {
    return { val: entry };
  }
  
  return { val: entry[0], gender: entry[1] };
};

